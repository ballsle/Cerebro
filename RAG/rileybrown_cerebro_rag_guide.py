"""## Step 7: Inspect What the Model Actually Sees

Transparency is key in RAG. Here's how to show exactly which chunks were retrieved for a query.
"""

def rag_query_verbose(query: str, k: int = 3):
    """RAG with full transparency — shows retrieved chunks before generating."""
    print(f"📌 Query: {query}\n")
    chunks = retrieve(query, k=k)
    print(f"📚 Retrieved {len(chunks)} chunks:")
    for i, c in enumerate(chunks, 1):
        print(f"   {i}. [{c['id']}] {c['title']}  (L2 score: {c['score']:.2f})")
    prompt = build_prompt(query, chunks)
    print(f"\n📝 Prompt sent to LLM ({len(prompt)} chars):\n")
    print(prompt[:600] + "...\n")
    response = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[{"role": "user", "content": prompt}],
        temperature=0.7,
    )
    answer = response.choices[0].message.content
    print(f"🏛️ Answer:\n{answer}")
    return answer

