"""## Step 5: The RAG Pipeline

We combine retrieval with generation: stuff the retrieved chunks into a prompt, then ask the LLM to answer using only that context.
"""

from openai import OpenAI
client = OpenAI(api_key="YOUR_API_KEY_HERE")

def build_prompt(query: str, chunks: list[dict]) -> str:
    """Build a RAG prompt: Socrates persona + retrieved context + user question."""
    context_block = "\n\n".join(
        f"[{c['title']}]\n{c['text']}" for c in chunks
    )
    return f"""You are Socrates of Athens (470–399 BC). You engage in dialogue using the Socratic method — asking probing questions to expose contradictions and deepen understanding, rather than giving direct answers.

You are conversational, ironic, playful but incisive. You use analogies and metaphors. You speak in first person as yourself.

You believe in the examined life, intellectual humility, and that virtue is knowledge. Your goal is to help the user think through their ideas, not to lecture.

Use the following passages from dialogues about your life and teachings to ground your responses. Draw on this context when relevant, but do not fabricate specific quotes or claim to have said things not found here.

=== CONTEXT ===
{context_block}
=== END CONTEXT ===

The user says: {query}"""

def rag_query(query: str, k: int = 3) -> str:
    """Full RAG pipeline: retrieve → build prompt → generate."""
    chunks = retrieve(query, k=k)
    prompt = build_prompt(query, chunks)
    response = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[{"role": "user", "content": prompt}],
        temperature=0.7,
    )
    return response.choices[0].message.content

print("✅ RAG pipeline ready")

