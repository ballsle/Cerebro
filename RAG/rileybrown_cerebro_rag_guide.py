import os

corpus_dir = "corpus/socrates"
documents = []
doc_id = 0

for filename in sorted(os.listdir(corpus_dir)):
    if not filename.endswith(".txt"):
        continue

    with open(os.path.join(corpus_dir, filename), "r", encoding="utf-8") as f:
        raw_text = f.read()

    # Split into chunks by double newline (paragraph-level)
    # This roughly preserves dialogue exchanges
    paragraphs = [p.strip() for p in raw_text.split("\n\n") if p.strip()]

    # Group small paragraphs together into ~500 word chunks
    current_chunk = ""
    for para in paragraphs:
        if len((current_chunk + "\n\n" + para).split()) > 500 and current_chunk:
            documents.append({
                "id": f"doc_{doc_id:04d}",
                "title": filename.replace(".txt", "").replace("_", " ").title(),
                "text": current_chunk.strip()
            })
            doc_id += 1
            current_chunk = para
        else:
            current_chunk += "\n\n" + para if current_chunk else para

    # Don't forget the last chunk
    if current_chunk.strip():
        documents.append({
            "id": f"doc_{doc_id:04d}",
            "title": filename.replace(".txt", "").replace("_", " ").title(),
            "text": current_chunk.strip()
        })
        doc_id += 1

print(f"✅ Loaded {len(documents)} chunks from {len(os.listdir(corpus_dir))} files")
