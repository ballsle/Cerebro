"""## Step 2: Embed Documents

We convert each text chunk into a **vector** (a list of numbers) that captures its semantic meaning.
Similar texts will have vectors that point in similar directions.
"""

from sentence_transformers import SentenceTransformer
import numpy as np

# Load a small, fast embedding model (downloads ~90MB on first run)
print("Loading embedding model...")
embedder = SentenceTransformer('all-MiniLM-L6-v2')

# Embed all documents
texts = [doc['text'] for doc in documents]
embeddings = embedder.encode(texts, show_progress_bar=True)

print(f"\n✅ Embedded {len(embeddings)} documents")
print(f"   Each embedding is a vector of {embeddings.shape[1]} dimensions")
print(f"   Embedding matrix shape: {embeddings.shape}")

"""## Step 3: Build a Vector Index

**FAISS** (Facebook AI Similarity Search) creates an index that lets us find the nearest neighbor vectors in milliseconds — even with millions of documents.
"""

import faiss

# Build a flat L2 (Euclidean distance) index
# For large corpora, use faiss.IndexIVFFlat for speed
dimension = embeddings.shape[1]
index = faiss.IndexFlatL2(dimension)

# FAISS requires float32
embeddings_f32 = embeddings.astype('float32')
index.add(embeddings_f32)

print(f"✅ FAISS index built")
print(f"   Index type:  Flat L2 (exact search)")
print(f"   Dimensions:  {dimension}")
print(f"   Total docs:  {index.ntotal}")

"""## Step 4: Retrieval Function

Given a question, we embed it and find the most similar chunks in our index.
"""

def retrieve(query: str, k: int = 3) -> list[dict]:
    """
    Embed the query and retrieve the top-k most similar documents.
    Returns a list of document dicts with an added 'score' field.
    """
    # Embed the query
    query_vec = embedder.encode([query]).astype('float32')

    # Search the FAISS index
    distances, indices = index.search(query_vec, k)

    # Collect matching documents
    results = []
    for dist, idx in zip(distances[0], indices[0]):
        doc = documents[idx].copy()
        doc['score'] = float(dist)   # Lower = more similar
        results.append(doc)

    return results


